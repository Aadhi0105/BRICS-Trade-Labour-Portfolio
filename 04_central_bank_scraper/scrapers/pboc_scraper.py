# People's Bank of China (PBOC) — Data Source Note
#
# PBOC data is sourced from the BIS CBSPEECHES database rather than direct scraping.
#
# Reason: The PBOC English website has sparse and inconsistently structured content.
# Post-meeting press releases are not published in a clean, accessible format in English.
# The PBOC communicates primarily through quarterly committee summaries rather than
# post-meeting press releases comparable to other BRICS central banks.
#
# The BIS CBSPEECHES database provides the most complete and consistent source
# of PBOC communications in English available for research purposes.
#
# See: notebook.ipynb Section 4 (PBOC) for download and processing details.
# Source: https://www.bis.org/cbspeeches/download.htm